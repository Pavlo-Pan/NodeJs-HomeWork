import mongoose from "mongoose";
const publisherSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        unique: true,
        trim: true
    },
    location: {
        type: String,
        required: true,
        trim: true
    },
    phone: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
        trim: true
    }
}, { timestamps: true });
const Publisher = mongoose.model("Publisher", publisherSchema); 
export default Publisher;
